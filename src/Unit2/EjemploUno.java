class EjemploUno {
	/* 1. ¿Cuál es el resultado del siguiente programa? */
	public static void main(String[] args) {
		int a = 1, b = 4, c = 2, d = 1;
		int x = a + b / c + d;
		System.out.print("x= " + x); // el + x significa que queremos mostrar el valor de la variable "x"
		// El resultado de x es 3
	}
}
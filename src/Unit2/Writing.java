package Unit2;

class Writing {
    public static void main(String[] args) {
        int i, j;
        i = 1;
        j = 3;
        System.out.print("Without end of line");
        System.out.print("i :" + i);
        System.out.print("j :" + j);
        System.out.println();
        System.out.println("With end of line");
        System.out.println("i :" + i);
        System.out.println("j :" + j);
    } // End of method main
} // End of class